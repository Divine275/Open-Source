package Problems;

public class Size_Of_DataTypes {
    public static void main(String[] args){
        System.out.println("Primitive/Fundamental DataTypes in JAVA : ");
        System.out.println();
        System.out.println("boolean");
        System.out.println("byte");
        System.out.println("char");
        System.out.println("short");
        System.out.println("int");
        System.out.println("long");
        System.out.println("float");
        System.out.println("double");
        System.out.println();
        System.out.println("Size of the above DataTypes are given below:");
        System.out.println();
        System.out.println("boolean : 1 bit");
        System.out.println("byte : 1 byte");
        System.out.println("char : 2 byte");
        System.out.println("short : 2 bytes");
        System.out.println("int : 4 bytes");
        System.out.println("long : 8 bytes");
        System.out.println("float : 4 bytes");
        System.out.println("double : 8 bytes");
    }
}


