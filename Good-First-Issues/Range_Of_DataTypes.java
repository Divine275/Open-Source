package Problems;

public class Range_Of_DataTypes {
    public static void main(String[] args){
        System.out.println("Primitive/Fundamental DataTypes in JAVA : ");
        System.out.println();
        System.out.println("boolean");
        System.out.println("byte");
        System.out.println("char");
        System.out.println("short");
        System.out.println("int");
        System.out.println("long");
        System.out.println("float");
        System.out.println("double");
        System.out.println();
        System.out.println("Range of the above DataTypes are given below:");
        System.out.println();
        System.out.println("boolean : Stores true or false values");
        System.out.println("byte : Stores whole numbers from -128 to 127");
        System.out.println("char : Stores a single character/letter or ASCII values");
        System.out.println("short : Stores whole numbers from -32,768 to 32,767");
        System.out.println("int : Stores whole numbers from -2,147,483,648 to 2,147,483,647");
        System.out.println("long : Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807");
        System.out.println("float : Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits");
        System.out.println("double : Stores fractional numbers. Sufficient for storing 15 decimal digits");
    }
}
